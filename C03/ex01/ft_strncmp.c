#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	m;

	m = 0;
	while (m < n)
	{
		if (s1[m] != s2[m] || s1[m] == '\0' || s2[m] == '\0')
		{
			return (s1[m] - s2[m]);
		}
		m++;
	}
	return (0);
}
