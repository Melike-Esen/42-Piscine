#include <unistd.h>

int	main(int ac, char **av)
{
	int	m;
	int	e;

	m = 1;
	while (m < ac)
	{
		e = 0;
		while (av[m][e])
		{
			write(1, &av[m][e], 1);
			e++;
		}
		write(1, "\n", 1);
		m++;
	}
	return (0);
}
