#include <unistd.h>

int	main(int ac, char **av)
{
	int	m;

	m = 0;
	if (ac == 1)
	{
		while (av[0][m] != '\0')
		{
			write(1, &av[0][m], 1);
			m++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
