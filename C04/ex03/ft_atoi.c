#include <stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	s;
	int	x;

	i = 0;
	s = 1;
	x = 0;
	while (str[i] >= 9 && str[i] <= 13 || str[i] == ' ')
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			s *= -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		x = (x * 10) + (str[i] - 48);
		i++;
	}
	return (x * s);
}
